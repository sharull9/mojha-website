            <section>
              <h2
                class="docs-article__section-title"
                id="what-is-a-rest-api-anyway"
              >
                <a
                  class="toclink"
                  href="#what-is-a-rest-api-anyway"
                  title="What is a REST API, anyway?"
                  >What is a REST API, anyway?</a
                >
              </h2>
              <p>
                <a
                  href="https://www.twilio.com/docs/glossary/what-is-an-api"
                  title="API"
                  >API</a
                >
                is short for ‘Application Programming Interface’ . An API is a
                set of rules that lets programs talk to each other, exposing
                data and functionality across the Internet in a consistent
                format.
              </p>
              <p>
                REST stands for ‘<a
                  href="http://www.ics.uci.edu/~fielding/pubs/dissertation/rest_arch_style.htm"
                  title="Representational State Transfer"
                  >Representational State Transfer</a
                >’. This is an architectural pattern that describes how
                distributed systems can expose a consistent interface. When
                people use the term ‘REST API’, they are generally referring to
                an API accessed using the HTTP protocol at a predefined set of
                URLs.
              </p>
              <p>
                These URLs represent various resources — any information or
                content accessed at that location, which can be returned as
                JSON, HTML, audio files, or images. Often resources have one or
                more methods that can be performed on them over HTTP, like
                <code>GET</code>, <code>POST</code>, <code>PUT</code>, and
                <code>DELETE</code>. The action represented by the first and
                last of these is clear, but <code>POST</code> and
                <code>PUT</code> have specific meanings. How they are defined is
                confusing, but the general rule is: use <code>POST</code> to
                create resources, and <code>PUT</code> to update resources.
              </p>
              <p>
                Twilio, for example, provides many separate REST APIs for
                sending
                <a class="inlinks" href="https://www.twilio.com/sms"
                  >text messages</a
                >, making phone calls, looking up phone numbers, managing your
                accounts, and a whole lot more. In Twilio’s ecosystem, each
                product is its own API, but you will work with each of them in
                roughly the same way, whether directly over HTTP or using
                <a
                  href="https://www.twilio.com/docs/libraries"
                  title="Twilio’s helper libraries"
                  >Twilio’s helper libraries</a
                >
                for several different programming languages.
              </p>
            </section>
